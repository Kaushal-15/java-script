let day = prompt();

if (day == 1) {
  alert("MONDAY");
} else if (day == 2) {
  alert("TUESDAY");
} else if (day == 3) {
  alert("WEDNESDAY");
} else if (day == 4) {
  alert("THURSDAY");
} else if (day == 5) {
  alert("FRIDAY");
} else if (day == 6) {
  alert("SATURDAY");
} else if (day == 7) {
  alert("SUNDAY");
} else {
  alert("Enter proper number between 1 to 7");
}

